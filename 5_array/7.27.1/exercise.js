const names = ["Luca", "Marco", "Vittorio", "Giovanni"];

names.pop();                 //pop toglie ultimo elemento
console.log(names);

names.push('Pippo');         //push aggiunge un elemento alla fine
console.log(names);

names.unshift('Giovanni');   //unshift aggiungiamo un elemento all'inizio
console.log(names);

console.log(names.join("-")); //join aggiunta la punteggiatura

